#Initialize Lists
friends = ["Bob", "Jill", "Jack", "Jim"]

#Printing lists
print(friends)
num_friends = len(friends)
print(num_friends)

#Indexing into lists (similar to strings)
print(friends[0]) 
print(friends[-1])
print(friends[1:])
print(friends[1:3])

#Modify lists
friends[1] = "Krish"
print(friends)

